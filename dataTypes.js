/**Primitive Data Type
   - Undefined
   - Null
   - Boolean
   - Number
   - String
- Complex Data Type
   - Object
   - function
 **/

 // Undefined Data Type
 var desh;
 console.log(typeof desh); // undefined

 // Null Data Type
 var desh = null;
 console.log(typeof desh); // Object

 // Boolean Data Type
 var desh = true;
 console.log(typeof desh); // boolean

 // Number Data type
 var desh = 7;
 console.log(typeof desh); // number

 // String Data Type
 var desh = "deepak";
 console.log(typeof desh); // string

 // function Data Type
 var desh = function(){
     return '';
 }
 console.log(typeof desh); // function

 // Object Data Type
 var desh = { "name": "deepak"}
 console.log(typeof desh); // object